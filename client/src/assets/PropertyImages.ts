// Function to get a specific image by index
export function getPropertyImageByIndex(index: number): string {
  // Make sure the index is within the array bounds
  const safeIndex = index % propertyImages.length;
  return propertyImages[safeIndex];
}

// Function to get a random property image
export function getRandomPropertyImage(): string {
  const randomIndex = Math.floor(Math.random() * propertyImages.length);
  return propertyImages[randomIndex];
}

// Function to get property images for the home page
export function getHomePagePropertyImages(count: number = 3): string[] {
  if (count > propertyImages.length) {
    // If requesting more images than available, just return all
    return [...propertyImages];
  }
  
  return propertyImages.slice(0, count);
}

